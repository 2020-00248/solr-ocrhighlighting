package org.mdz.search.solrocr.util;

public class OcrBox {
  public final float x;
  public final float y;
  public final float width;
  public final float height;


  public OcrBox(float x, float y, float width, float height) {
    this.x = x;
    this.y = y;
    this.width = width;
    this.height = height;
  }
}
